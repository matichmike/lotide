const findKeyByValue = function(showsList, theMovie) {
  let result;
  const categories = Object.keys(showsList);
  for (let category of categories) {
    if (showsList[category] === theMovie) {
      result = category;
    }
  }
  return result;
}

const bestTVShowsByGenre = { 
  sci_fi: "The Expanse",
  comedy: "Brooklyn Nine-Nine",
  drama:  "The Wire"
};

assertEqual(findKeyByValue(bestTVShowsByGenre, "The Wire"), "drama");
